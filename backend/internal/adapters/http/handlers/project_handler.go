func (h *ProjectHandler) CreateChildCategory(c *gin.Context) {
	var req domain.ChildCategory
	if err := c.ShouldBindJSON(&req); err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": err.Error()})
		return
	}
	if req.Name == "" {
		c.JSON(http.StatusBadRequest, gin.H{"error": "Name is required"})
		return
	}
	// Require either MainCategoryID OR StationID
	if req.MainCategoryID == uuid.Nil && req.StationID == nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": "Either MainCategoryID or StationID is required"})
		return
	}
	
	// Auto-generate column_key from name
	req.ColumnKey = toSnakeCase(req.Name)
	
	// Create child category in DB
	if err := h.Repo.CreateChildCategory(&req); err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": err.Error()})
		return
	}
	
	// Auto-create column in project_characteristics table
	if h.DB != nil && req.ColumnKey != "" {
		sql := fmt.Sprintf(
			"ALTER TABLE project_characteristics ADD COLUMN IF NOT EXISTS %s INTEGER DEFAULT 0",
			req.ColumnKey,
		)
		if err := h.DB.Exec(sql).Error; err != nil {
			// Log but don't fail - column might already exist
			fmt.Printf("Warning: Could not create column %s: %v\n", req.ColumnKey, err)
		}
	}
	
	c.JSON(http.StatusCreated, req)
}

// UpdateChildCategory handles PUT /child-categories/:id
func (h *ProjectHandler) UpdateChildCategory(c *gin.Context) {
	idStr := c.Param("id")
	id, err := uuid.Parse(idStr)
	if err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": "Invalid Child Category ID"})
		return
	}
	
	var req struct {
		Name             string  `json:"name"`
		RequiresInverter *bool   `json:"requires_inverter"`
		StationID        *string `json:"station_id"` // Can be null to unlink
	}
	if err := c.ShouldBindJSON(&req); err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": err.Error()})
		return
	}
	
	// Update in database
	updates := map[string]interface{}{}
	if req.Name != "" {
		updates["name"] = req.Name
		updates["column_key"] = toSnakeCase(req.Name)
	}
	if req.RequiresInverter != nil {
		updates["requires_inverter"] = *req.RequiresInverter
	}
	// Handle station_id (can be null to unlink, or a UUID string to link)
	if req.StationID != nil {
		if *req.StationID == "" {
			updates["id_station"] = nil
		} else {
			stationUUID, err := uuid.Parse(*req.StationID)
			if err == nil {
				updates["id_station"] = stationUUID
			}
		}
	}
	
	if h.DB != nil {
		if err := h.DB.Model(&domain.ChildCategory{}).Where("id = ?", id).Updates(updates).Error; err != nil {
			c.JSON(http.StatusInternalServerError, gin.H{"error": err.Error()})
			return
		}
	}
	
	c.JSON(http.StatusOK, gin.H{"message": "Child category updated successfully", "id": id})
}


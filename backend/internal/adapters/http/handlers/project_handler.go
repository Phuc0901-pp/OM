func (h *ProjectHandler) UpdateProjectCharacteristics(c *gin.Context) {
	idStr := c.Param("id")
	id, err := uuid.Parse(idStr)
	if err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": "Invalid Project ID"})
		return
	}

	// Accept flexible map from frontend
	var req map[string]interface{}
	if err := c.ShouldBindJSON(&req); err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": err.Error()})
		return
	}

	// Get existing or create new record
	existing, _ := h.Repo.GetProjectCharacteristic(id)
	if existing == nil {
		existing = &domain.ProjectCharacteristic{
			ProjectID: id,
		}
	}

	// Parse existing ChildCategoryData
	existingData := make(map[string]interface{})
	if existing.ChildCategoryData != nil {
		json.Unmarshal(existing.ChildCategoryData, &existingData)
	}

	// Merge new data into existing
	for key, value := range req {
		switch key {
		case "inverter_sub_area_count":
			if v, ok := value.(float64); ok {
				existing.InverterSubAreaCount = int(v)
			}
		case "inverter":
			if v, ok := value.(float64); ok {
				existing.Inverter = int(v)
			}
		case "inverter_details":
			if bytes, err := json.Marshal(value); err == nil {
				existing.InverterDetails = bytes
			}
		default:
			// All other keys (process_ids, area_name, quantities) go into child_category_data JSON
			existingData[key] = value
		}
	}

	// Marshal back to JSON
	if dataBytes, err := json.Marshal(existingData); err == nil {
		existing.ChildCategoryData = dataBytes
	}

	if err := h.Repo.UpdateProjectCharacteristic(id, existing); err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": err.Error()})
		return
	}
	c.JSON(http.StatusOK, existing)
}

